package com.gazi.gorsel.sinema.model;

/**
 *
 * @author emin
 */
public class Salon {
    private int id, sira, genislik;
    private String ad;

    public void setSira(int sira) {
        this.sira = sira;
    }

    public int getSira() {
        return sira;
    }

    public int getGenislik() {
        return genislik;
    }

    public void setGenislik(int genislik) {
        this.genislik = genislik;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }
}
