    private void btnKoltukSecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKoltukSecActionPerformed
        if (datePicker.getDate().before(Date.from(Instant.now()))) {
            JOptionPane.showMessageDialog(null, "Geçmişe dönük seçim yapılamaz.");
            datePicker.setDate(Date.from(Instant.now()));
            return;
        }
        DateFormat myFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = datePicker.getDate();
        String dt = myFormat.format(date);
        SalonBL sbl = new SalonBL();
        FilmBL fbl = new FilmBL();
        SeansBL snsbl = new SeansBL();
        Salon s = sbl.SalonGetir(cmbSalon.getSelectedItem().toString());
        Seans sns = snsbl.SeansGetir(cmbSeans.getSelectedItem().toString());
        Film film = fbl.FilmGetir(cmbFilm.getSelectedItem().toString());
        frmKoltuk f = new frmKoltuk(dt, s, sns, film);
        pnlMain.add(f);
        f.setBounds(450, 0, 1000, 800);
        f.setVisible(true);
        f.setClosable(true);
    }//GEN-LAST:event_btnKoltukSecActionPerformed


