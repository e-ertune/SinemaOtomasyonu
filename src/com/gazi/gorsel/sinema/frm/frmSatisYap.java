    private void formİnternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formİnternalFrameOpened
        FilmBL fbl = new FilmBL();
        SeansBL sbl = new SeansBL();
        SalonBL slbl = new SalonBL();
        List<Film> filmList = fbl.FilmListele();
        List<Seans> seansList = sbl.SeansListele();
        List<Salon> salonList = slbl.SalonListele();
        
        for (int i = 0; i < filmList.size(); i++) {
            cmbFilm.addItem(filmList.get(i).getAd());
        }
        for (int i = 0; i < seansList.size(); i++) {
            cmbSeans.addItem(seansList.get(i).getSaat());
        }
        for (int i = 0; i < salonList.size(); i++) {
            cmbSalon.addItem(salonList.get(i).getAd());
        }
        datePicker.setDate(Date.from(Instant.now()));
    }//GEN-LAST:event_formİnternalFrameOpened

